package com.military.bean;

public class Posting {

	private String ms_id;
	private String posting_id;
	private String period_of_work;
	private String area;
	private String camp_name;
	private String dateof_work;
	public String getMs_id() {
		return ms_id;
	}
	public void setMs_id(String ms_id) {
		this.ms_id = ms_id;
	}
	public String getPosting_id() {
		return posting_id;
	}
	public void setPosting_id(String posting_id) {
		this.posting_id = posting_id;
	}
	public String getPeriod_of_work() {
		return period_of_work;
	}
	public void setPeriod_of_work(String period_of_work) {
		this.period_of_work = period_of_work;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getCamp_name() {
		return camp_name;
	}
	public void setCamp_name(String camp_name) {
		this.camp_name = camp_name;
	}
	public String getDateof_work() {
		return dateof_work;
	}
	public void setDateof_work(String dateof_work) {
		this.dateof_work = dateof_work;
	}
	  
	 
}
