package com.military.bean;

public class Medicalcamp {

	
	private String ms_id;
	private String  patient_id;
	private String area;
	private String camp_name;
	private String health_condition;
	public String getMs_id() {
		return ms_id;
	}
	public void setMs_id(String ms_id) {
		this.ms_id = ms_id;
	}
	public String getPatient_id() {
		return patient_id;
	}
	public void setPatient_id(String patient_id) {
		this.patient_id = patient_id;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getCamp_name() {
		return camp_name;
	}
	public void setCamp_name(String camp_name) {
		this.camp_name = camp_name;
	}
	public String getHealth_condition() {
		return health_condition;
	}
	public void setHealth_condition(String health_condition) {
		this.health_condition = health_condition;
	}
	 
	 
}
